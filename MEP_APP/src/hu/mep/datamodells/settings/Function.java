package hu.mep.datamodells.settings;

public class Function {
	
	public int serialNumber;
	
	public boolean status;
	
	public String name;
	
	public String label;

	public Function(int serialNumber, boolean status, String name, String label) {
		super();
		this.serialNumber = serialNumber;
		this.status = status;
		this.name = name;
		this.label = label;
	}
	
	

	
	
}
